bool HelloTriangleApplication::checkRequiredExtensionsPresent(std::vector<VkExtensionProperties> & suppExtension, const char** extensions, int size_extensions) {
	bool result = true;
	for (int i = 0; i < size_extensions; i++) {

		bool extension_found = false;
		for (const auto& supportedExtension : suppExtension) {
			if (!strcmp(supportedExtension.extensionName, extensions[i])) {
				extension_found = true;
				break;
			}

		}
		if (extension_found == false) {
			result = false;
#ifdef _DEBUG
			std::cout << "Extension " << extensions[i] << " not supported" << std::endl;
#endif
		}

	}
	return result;
}

bool HelloTriangleApplication::checkValidationLayerSupport() {
	uint32_t layerCount;
	vkEnumerateInstanceLayerProperties(&layerCount, nullptr);					//Get number of layers

	std::vector<VkLayerProperties> availableLayers(layerCount);
	vkEnumerateInstanceLayerProperties(&layerCount, availableLayers.data());	//Get supported layers

	for (const char* layerName : validationLayers) {
		bool layerFound = false;

		for (const auto& layerProperties : availableLayers) {
			if (strcmp(layerName, layerProperties.layerName) == 0) {
				layerFound = true;
				break;
			}
		}

		if (!layerFound) {
			return false;
		}
	}

	return true;
}

